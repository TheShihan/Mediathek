namespace MediathekClient.Common
{

    /// <summary>
    /// Specifies the different MessageBox types
    /// </summary>
    public enum MessageBoxType
    {
        /// <summary>
        /// Error MessageBox. Offers a "OK" button
        /// </summary>
        Error,
        /// <summary>
        /// Info MessageBox. Offers a "OK" button
        /// </summary>
        Info,
        /// <summary>
        /// Question MessageBox. Offers a "YES" and "NO" button
        /// </summary>
        Question,
        /// <summary>
        /// Warning MessageBox. Offers a "YES" and "NO" button
        /// </summary>
        Warning
    }

}